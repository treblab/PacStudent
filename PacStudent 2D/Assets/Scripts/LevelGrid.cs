    public Vector2Int WorldToGridPosition(Vector3 worldPosition)
    {
        int x = Mathf.FloorToInt(worldPosition.x);
        int y = -Mathf.FloorToInt(worldPosition.y); // multiplying by -1 due to grid's WorldPosition.
        // Debug.Log("Grid position: " + new Vector2Int(x, y));
        return new Vector2Int(x, y);
    }

    public bool IsWalkable(Vector2Int gridPosition)
    {
        //if (isOutOfBounds(gridPosition))
        //{
           // return false;
        //}

        int value = levelMap[gridPosition.y, gridPosition.x];
        Debug.Log("IsWalkable value: " + value);

        return value == 0 || value == 5 || value == 6;
    }

    public Vector3 TryMove(Vector2 direction, Vector3 currentWorldPosition)
    {
        Vector2Int currentGridPosition = WorldToGridPosition(currentWorldPosition);
        Debug.Log("Current grid position in TryMove: " + currentGridPosition);
        Vector2Int targetGridPosition = currentGridPosition + new Vector2Int((int)direction.x, (int)direction.y);
        Debug.Log("targetGridPosition in TryMove: " + targetGridPosition);

        if (IsWalkable(targetGridPosition))
        {
            // Debug.Log("Position is walkable. ");
            return new Vector3(targetGridPosition.x, targetGridPosition.y, currentWorldPosition.z);
        }
        // Debug.Log("Position is NOT walkable. " + targetGridPosition); // issue is here. pos should be walkable.
        return currentWorldPosition; // Return the current position if the move is not valid
    }

    private bool isOutOfBounds(Vector2Int gridPosition)
    {
        if (gridPosition.x < 0 || gridPosition.x >= levelMap.GetLength(1) ||
            -gridPosition.y < 0 || -gridPosition.y >= levelMap.GetLength(0))
        {
            Debug.Log("Out of bounds grid position: " + gridPosition.x + ", " + gridPosition.y);
            // Out of bounds
            return true;
        }
        return false;
    }
