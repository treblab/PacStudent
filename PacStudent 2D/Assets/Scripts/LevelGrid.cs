    public Vector2Int WorldToGridPosition(Vector3 worldPosition)
    {
        int x = Mathf.FloorToInt(worldPosition.x);
        int y = Mathf.FloorToInt(worldPosition.y);
        return new Vector2Int(x, y);
    }

    public bool IsWalkable(Vector2Int gridPosition)
    {
        if (gridPosition.x < 0 || gridPosition.x >= levelMap.GetLength(1) || gridPosition.y < 0 || gridPosition.y >= levelMap.GetLength(0))
        {
            // Out of bounds
            return false;
        }

        int value = levelMap[gridPosition.y, gridPosition.x];
        return value == 0 || value == 5 || value == 6;
    }

    public Vector3 TryMove(Vector2 direction, Vector3 currentWorldPosition)
    {
        Vector2Int currentGridPosition = WorldToGridPosition(currentWorldPosition);
        Vector2Int targetGridPosition = currentGridPosition + new Vector2Int((int)direction.x, (int)direction.y);

        if (IsWalkable(targetGridPosition))
        {
            return new Vector3(targetGridPosition.x, targetGridPosition.y, currentWorldPosition.z);
        }
        return currentWorldPosition; // Return the current position if the move is not valid
    }

